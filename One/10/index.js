// Um ciclista sobe a montanha a uma velocidade de 20 Km/h e a desce pelo mesmo
// caminho a 60 Km/h. Admitindos-e que ele não demore tempo algum no topo, qual a
// velocidade média de todo o passeio?

const velocity_up = 20;
const velocity_down = 60;
const result = (velocity_up + velocity_down) / 2;

console.log("A velocidade média de todo o passeio é de " + result + "KM/H");
